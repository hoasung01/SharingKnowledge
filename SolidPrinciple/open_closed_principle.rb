module SolidPrinciple
  class OpenClosedPrinciple
    def define
      <<~DEFINE.strip
        The Open/Closed principle states software entities like classes,
        modules, functions, etc. should be open for extension, but closed for
        modification. that is, such an entity can allow its behaviour to be
        extended without modifying its source code.
      DEFINE
    end
  end
end
